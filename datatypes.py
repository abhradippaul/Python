print("Python Data Types with Description")
print("-" * 40)

# Integer: stores whole numbers
integer_value = 10
print("int:", integer_value, "- Whole numbers without decimals")

# Float: stores decimal numbers
float_value = 10.5
print("float:", float_value, "- Numbers with decimal points")

# Complex: stores numbers with real and imaginary parts
complex_value = 2 + 3j
print("complex:", complex_value, "- Numbers with real and imaginary parts")

# String: stores text
string_value = "Hello, Python"
print("str:", string_value, "- Sequence of characters or text")

# Boolean: stores True or False
boolean_value = True
print("bool:", boolean_value, "- Logical values: True or False")

# List: ordered, changeable collection
list_value = [1, 2, 3, "apple"]
print("list:", list_value, "- Ordered and changeable collection")

# Tuple: ordered, unchangeable collection
tuple_value = (1, 2, 3, "banana")
print("tuple:", tuple_value, "- Ordered and unchangeable collection")

# Set: unordered collection of unique values
set_value = {1, 2, 3, 4}
print("set:", set_value, "- Unordered collection with unique items")

# Dictionary: stores data in key-value pairs
dict_value = {"name": "Abhra", "age": 20}
print("dict:", dict_value, "- Data stored in key-value pairs")

# Range: sequence of numbers
range_value = range(5)
print("range:", list(range_value), "- Sequence of numbers in a range")

# NoneType: represents no value
none_value = None
print("NoneType:", none_value, "- Represents no value or empty value")

# Bytes: immutable sequence of bytes
bytes_value = b"hello"
print("bytes:", bytes_value, "- Immutable binary data")

# Bytearray: mutable sequence of bytes
bytearray_value = bytearray(5)
print("bytearray:", bytearray_value, "- Mutable binary data")

# Frozenset: immutable set
frozenset_value = frozenset({1, 2, 3})
print("frozenset:", frozenset_value, "- Unchangeable set of unique items")
